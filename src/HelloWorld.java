public class HelloWorld {
    public static void main(String[] args) {
        String message = "Hello, World!"; // String variable
        int number = 42;                 // Integer variable
        double pi = 3.14159;             // Double variable
        char letter = 'H';               // Character variable
        boolean isJavaFun = true;        // Boolean variable

        System.out.println(message);
        System.out.println("Number: " + number);
        System.out.println("Value of Pi: " + pi);
        System.out.println("First Letter: " + letter);
        System.out.println("Is Java fun? " + isJavaFun);
    }
}
